package codigo;

public class primero {

	private int num1=0;
	private int num2=0;
	
	public primero(int num1, int num2){
		this.num1=0;
		this.num2=0;
	}
	public primero(){
		this.num1=0;
		this.num2=0;
	}
	public void setprimero(int num1){
		this.num1=num1;
	}
	
	public void setprimero2(int num2){
		this.num2=num2;
	}
	
	public int suma()
	{
		return this.num1+this.num2;}
	
	public int resta()
	{
		return this.num1-this.num2;

	}

	public int multiplica()
{
	return this.num1+this.num2;}

}

